            <h5>Requisitos para doação de sangue:</h5>
            <ul>
                <li>Estar em boas condições de saúde;</li>
<li>Apresentar documento oficial de identidade com foto;</li>
<li>Ter idade entre 16 e 69 anos, sendo que os candidatos a doadores com menos de 18 anos deverão estar acompanhados pelos pais ou por responsável legal;</li>
<li>Pesar no mínimo 50 Kg com desconto de vestimentas;</li>
<li>O limite de idade para a primeira doação é de 60 anos;</li>
<li>Não estar em jejum e evitar alimentos gordurosos nas três horas que antecedem a doação. Caso seja depois do almoço, aguardar duas horas;</li>
<li>Ter dormido pelo menos 6 horas antes da doação;</li>
<li>Não ter ingerido bebidas alcoólicas nas 12 horas anteriores à doação;</li>
<li>Não fumar pelo menos duas horas antes da doação.</li>
<li>Mulheres: período de 90 dias/máximo de 3 doações nos últimos 12 meses;</li>
<li>Homens: período de 60 dias/máximo de 4 doações nos últimos 12 meses.</li>
            </ul>
            
            <h5>Impedimentos temporários</h5>
            <ul>
<li>Gripe ou febre;</li>
<li>Gestantes ou mães que amamentam bebes com menos de 12 meses;</li>
<li>Até 90 dias após aborto ou parto normal e até 180 dias após cesariana;</li>
<li>Tatuagem ou acupuntura nos últimos 12 meses;</li>
<li>Exposição à situação de risco para a AIDS (múltiplos parceiros sexuais, ter parceiros usuários de drogas);</li>
<li>Herpes labial.</li>
            </ul>
            
    <h5>Impedimentos definitivos</h5>
    <ul>
<li>Doença de Chagas;</li>
<li>Hepatite após os 11 anos de idade;</li>
<li>Ser portador dos vírus HIV (AIDS), HCV (Hepatite C), HBC (Hepatite B), HTLV;</li>
<li>Uso de drogas injetáveis.</li>
<li>Outros critérios que impedem a doação serão verificados por ocasião da entrevista de triagem</li>
    </ul>
  
    
        <h5>O que fazer depois da doação</h5>
        <ul>
<li>Não fumar por no mínimo duas horas;</li>
<li>Nas 12 horas após a doação, não praticar exercícios físicos e atividades perigosas, como subir em locais altos ou dirigir caminhão, ônibus em rodovias, etc.</li>
<li>Permanecer no serviço hemoterápico após a doação por 15 minutos;</li>
<li>Não carregar peso ou dobrar o braço em que foi realizada a punção no dia da doação, para evitar sangramentos e hematomas;</li>
<li>Retirar o curativo 4 horas após a doação.</li>
    </ul>
    
    <h5>Etapas da coleta de sangue:</h5>
    <ol>
<li>Triagem: Verificação dos sinais vitais (pressão arterial, temperatura e batimentos cardíacos), peso e teste de anemia.</li>
<li>Triagem clínica: Entrevista individual e sigilosa onde serão avaliados os antecedentes e o estado atual de saúde do candidato à doação para determinar se a coleta poderá trazer ricos para ele ou para o receptor.</li>
<li>Coleta de Sangue: Coleta de aproximadamente 450 mililítros de sangue e amostras para realização dos testes laboratoriais.</li>
<li>Hideatação: Após a doação de sangue, o doador recebe um lache e é recomendável que o voluntário permaneça no mínimo 15 minutos no Hemocentro e beba bastante líquido durante o dia.</li>
    </ol>
